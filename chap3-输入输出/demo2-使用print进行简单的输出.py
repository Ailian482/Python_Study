"""
基本的输出函数 print()
    - 基本语法结构
      print(输出内容)
    - 使用 print() 函数进行简单输出
      · 输出内容可以是数字和字符串（字符串需要引号括起来），此内容将直接输出，也可以是包含运算符的表达式，此类内容将计算结果输出
      · 如果想要一次输出多个内容，且不换行，则可以使用英文半角的逗号将要输出的内容分隔
"""

a = 100
b = 50
print(a)
print(b)
print(a+b)
print("北京欢迎你！！")
print('北京欢迎你！！')
print("""北京欢迎你！！！""")
print(a, b, "北京欢迎你！！！")
