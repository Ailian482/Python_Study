# 1-100之间是累加和
s = 0  # 存储累加和
i = 1  # 1. 初始化变量
while i <= 100:  # 2.条件判断
    s += i  # 3. 语句块
    i += 1  # 4. 改变变量
print("1-100之间的累加和为：", s)
